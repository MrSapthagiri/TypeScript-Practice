// 1. Create a named function to greet user in the morning.
function greetMorning(name: string): string {
    return `Good morning, ${name}!`;
}
console.log(greetMorning("Arun"));

// 2. Create a named function to greet user in the evening.
function greetEvening(name: string): string {
    return `Good evening, ${name}!`;
}
console.log(greetEvening("Meena"));

// 3. Create a named function to greet user at night.
function greetNight(name: string): string {
    return `Good night, ${name}!`;
}
console.log(greetNight("Kavi"));

// 4. Create a named function to greet user formally.
function greetFormal(name: string): string {
    return `Dear ${name}, welcome.`;
}
console.log(greetFormal("John"));

// 5. Create a named function to greet user casually.
function greetCasual(name: string): string {
    return `Hey ${name}, what's up?`;
}
console.log(greetCasual("Sam"));
