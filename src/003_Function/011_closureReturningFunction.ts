// 1. Create a named function to double a value.
function doubleValue(x: number): number {
    return x * 2;
}
console.log("Double:", doubleValue(10));

// 2. Create a named function to triple a value.
function tripleValue(x: number): number {
    return x * 3;
}
console.log("Triple:", tripleValue(10));

// 3. Create a named function to square a value.
function squareValue(x: number): number {
    return x * x;
}
console.log("Square:", squareValue(10));

// 4. Create a named function to increment value by 10.
function incrementByTen(x: number): number {
    return x + 10;
}
console.log("Increment:", incrementByTen(10));

// 5. Create a named function to decrement value by 5.
function decrementByFive(x: number): number {
    return x - 5;
}
console.log("Decrement:", decrementByFive(10));
