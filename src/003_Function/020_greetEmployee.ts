// 1. Create a named function to greet an employee.
function greetEmployee(name: string): string {
    return `Hello, ${name}`;
}
console.log(greetEmployee("John"));

// 2. Create a named function to greet a manager.
function greetManager(name: string): string {
    return `Good day, Manager ${name}`;
}
console.log(greetManager("Meena"));

// 3. Create a named function to greet a developer.
function greetDeveloper(name: string): string {
    return `Welcome Developer ${name}`;
}
console.log(greetDeveloper("Arun"));

// 4. Create a named function to greet a tester.
function greetTester(name: string): string {
    return `Hello QA Engineer ${name}`;
}
console.log(greetTester("Kavi"));

